function updateLanguageWithStoredValue(selectedLanguage) {
  const nodes = document.querySelectorAll("[data-lang]");
  nodes.forEach((node) => {
    const key = node.getAttribute("data-lang");
    node.innerText = lang[selectedLanguage][key];
    node.classList.toggle("reduced-font", selectedLanguage === "ru");
  });
}

document.addEventListener("DOMContentLoaded", function () {
  // Fetch the selected language from sessionStorage or use 'en' as the default
  const selectedLanguage = sessionStorage.getItem("selectedLanguage") || "en";
  updateLanguageWithStoredValue(selectedLanguage);
  localStorage.setItem("selectedLanguage", selectedLanguage);
});

const selectorButtons = document.querySelectorAll("#Language-selector");
selectorButtons.forEach((button) => {
  button.addEventListener("click", function (event) {
    const selectedLanguage = event.target.getAttribute("data-lang");
    updateLanguageWithStoredValue(selectedLanguage);
    // Update the language query parameter in the URL
    sessionStorage.setItem("selectedLanguage", selectedLanguage || "en");
    // updateQueryStringParameter('lang', selectedLanguage);
  });
});

function getQueryStringParameter(name) {
  const urlParams = new URLSearchParams(window.location.search);
  return urlParams.get(name);
}

function updateQueryStringParameter(key, value) {
  const urlParams = new URLSearchParams(window.location.search);
  urlParams.set(key, value);
  const newUrl =
    window.location.protocol +
    "//" +
    window.location.host +
    window.location.pathname +
    "?" +
    urlParams.toString();
  history.pushState({ path: newUrl }, "", newUrl);
}
